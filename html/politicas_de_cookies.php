    <section id="uso-cookies">
      <h2>1) Utilización de Cookies y Web Bugs</h2>
      <p>
        Las webs de EXGADET S.A. (en adelante, “las Webs”) utilizan “Cookies” y otros mecanismos similares
        (en adelante, “Cookies”). Las Cookies son ficheros enviados a su navegador por un servidor web para
        registrar la actividad del Usuario en una web determinada o en los Servicios (webs, apps y/o servicios).
      </p>
      <p>
        La finalidad principal de las Cookies es facilitar un acceso más rápido a los Servicios seleccionados y
        personalizar su experiencia, ofreciendo información de su interés en función del uso que realiza. Las
        Cookies se asocian únicamente a un Usuario anónimo y su dispositivo; no proporcionan referencias que
        permitan deducir datos personales.
      </p>
      <p>
        El Usuario puede configurar su navegador para notificar y rechazar la instalación de Cookies enviadas
        por las Webs sin que ello impida el acceso a los contenidos; no obstante, la calidad del funcionamiento
        puede verse afectada. Los Usuarios registrados podrán disfrutar de servicios más personalizados al combinar
        datos de cookies con sus datos de registro, sin perjuicio de su derecho a rechazar o deshabilitar su uso.
      </p>
      <p>
        Las Webs también pueden utilizar <strong>Web Bugs</strong> (imágenes diminutas y transparentes) insertos
        en emails. Al abrir el email, la imagen se descarga y permite conocer si se abrió el mensaje y desde qué IP,
        con fines estadísticos y analíticos sobre la recepción de comunicaciones.
      </p>
    </section>

    <!-- 2) Tipología -->
    <section id="tipos-finalidad">
      <h2>2) Tipología, finalidad y funcionamiento de las Cookies</h2>

      <p><span class="tag">Sesión</span><span class="tag">Persistentes</span></p>
      <p>
        Según su permanencia, las Cookies pueden ser de <strong>sesión</strong> (expiran al cerrar el navegador)
        o <strong>persistentes</strong> (expiran al cumplirse su objetivo o al borrarse manualmente).
      </p>

      <details>
        <summary>Cookies de rendimiento</summary>
        <p>
          Recuerdan sus preferencias de herramientas/Servicios, evitando que deba reconfigurarlos en cada visita.
        </p>
      </details>

      <details>
        <summary>Cookies de geolocalización</summary>
        <p>
          Determinan en qué país se encuentra al solicitar un Servicio para orientar contenido por ubicación.
          Son anónimas.
        </p>
      </details>

      <details>
        <summary>Cookies de registro</summary>
        <ul>
          <li>Mantener al Usuario identificado en posteriores accesos, salvo que seleccione “cerrar sesión”.</li>
          <li>Comprobar si el Usuario está autorizado para acceder a ciertos Servicios.</li>
          <li>
            Conectores con redes sociales (p.ej., Facebook/Twitter): si se registra con credenciales sociales,
            la red guarda una cookie persistente que recuerda su identidad hasta su expiración (puede revocar el
            acceso en la propia red social).
          </li>
        </ul>
      </details>

      <details>
        <summary>Cookies analíticas</summary>
        <p>
          Terceros (p. ej., Omniture, Netscope, Comscore, etc.) generan una cookie analítica para identificar de
          forma anónima visitas, contenidos más consultados, tendencias y si el Usuario es nuevo o recurrente.
          No se asocian a datos personales; uso estadístico para optimizar la experiencia.
        </p>
      </details>

      <details>
        <summary>Cookies de publicidad</summary>
        <p>
          Amplían la información de anuncios (frecuencia, interacción, patrones de navegación) para conformar
          perfiles de interés y ofrecer publicidad afín.
        </p>
      </details>

      <details>
        <summary>Cookies publicitarias de terceros</summary>
        <p>
          Además de la publicidad gestionada por las Webs, se pueden mostrar anuncios mediante plataformas de
          terceros (Ad-Servers) que almacenan cookies y acceden a sus datos. Actualmente puede utilizarse
          <strong>DoubleClick (Google)</strong>. Más info:
          <a href="https://www.google.es/policies/privacy/" target="_blank" rel="noopener">Política de Google</a>.
        </p>
      </details>
    </section>

